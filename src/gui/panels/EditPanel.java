    private void jButtonAdaugaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdaugaActionPerformed
        if (jpanelParent.getFileNameStr().length() != 0) {
            infoDao = InfoDaoImpl.getInstance();
            Info info = new Info();
            info.setTags(jTextFieldTaguri.getText());
            info.setTimpIn(jpanelParent.getTimeInStr());
            info.setTimpOut(jpanelParent.getTimeOutStr());
            info.setComments(jTextAreaDescriere.getText());
            info.setFisierNume(jpanelParent.getFileNameStr());
            info.setAnotimp((String) jComboBoxAnotimpuri.getSelectedItem());

            infoDao.saveInfo(info);
            clearFields();

        } else {
            JOptionPane.showMessageDialog(null, "Incarcati fisierul video!");
        }
    }//GEN-LAST:event_jButtonAdaugaActionPerformed

