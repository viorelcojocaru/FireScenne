    public void incarcaDateleInComp(String idStr) {
        infoDao = InfoDaoImpl.getInstance();
        Info info = new Info();

        info = infoDao.findById(idStr);
        String anotimp = info.getAnotimp();
        String time_in = info.getTimpIn();
        String time_out = info.getTimpOut();
        String comments = info.getComments();
        String tags = info.getTags();
        String filepath = info.getFisierNume();
        jButtonUpdate.setEnabled(true);
        jButtonAdauga.setEnabled(false);
        jTextAreaDescriere.setText(comments);
        jTextFieldTaguri.setText(tags);
        jpanelParent.setTime_In(time_in);
        jpanelParent.setTime_Out(time_out);
        jpanelParent.pornesteClipul(filepath, time_in, time_out);
        String[] anotimpuri = {"IARNA", "PRIMAVARA", "VARA", "TOAMNA"};

        for (int i = 0; i < anotimpuri.length; i++) {
            if (anotimpuri[i].equals(anotimp)) {
                jComboBoxAnotimpuri.setSelectedIndex(i);
            }
        }

    }
